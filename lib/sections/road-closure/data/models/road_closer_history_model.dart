import 'package:advice/sections/road-closure/domain/entities/road_closer_history_entity.dart';

class RoadCloserHistoryModel extends RoadCloserHistoryEntity {
  const RoadCloserHistoryModel({
    required super.id,
    required super.dateCreated,
    required super.latitude,
    required super.longitude,
    required super.roadName,
    required super.closureReason,
    required super.repairEta,
    required super.roadRefno,
    required super.contactPerson,
    required super.effortsBeingMade,
    required super.remarks,
    required super.district,
    required super.actualRepairTime,
    required super.dateRoadblockStart,
    required super.dateRoadblockEndEstimated,
    required super.dateRoadblockEnd,
    required super.createdByUserId,
    required super.lastUpdatedByUserId,
    required super.closureType,
    required super.chainage,
    required super.linkCode,
    required super.createdByUserName,
    required super.lastUpdatedByUserName,
    required super.images,
  });

  factory RoadCloserHistoryModel.fromJson(Map<String, dynamic> json) =>
      RoadCloserHistoryModel(
        id: json['id'],
        dateCreated: json['date_created'],
        latitude: json['latitude'],
        longitude: json['longitude'],
        roadName: json['road_name'],
        closureReason: json['closure_reason'],
        repairEta: json['repair_eta'],
        roadRefno: json['road_refno'],
        contactPerson: json['contact_person'],
        effortsBeingMade: json['efforts_being_made'],
        remarks: json['remarks'],
        district: json['district'],
        actualRepairTime: json['actual_repair_time'],
        dateRoadblockStart: json['date_roadblock_start'],
        dateRoadblockEndEstimated: json['date_roadblock_end_estimated'],
        dateRoadblockEnd: json['date_roadblock_end'],
        createdByUserId: json['created_by_user_id'],
        lastUpdatedByUserId: json['last_updated_by_user_id'],
        closureType: json['closure_type'],
        chainage: json['chainage'],
        linkCode: json['link_code'],
        createdByUserName: json['created_by_user_name'],
        lastUpdatedByUserName: json['last_updated_by_user_name'],
        images: json['images'].cast<String>(),
      );
}
